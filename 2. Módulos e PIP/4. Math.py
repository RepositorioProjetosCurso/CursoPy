import math

# 1 - Acessar número PI
print(math.pi)
print(f'{math.pi:.5f}')

# 2 - Acessar o número de Euler
print(math.e)
print(f'{math.e:.3f}')

# 3 - Arredonda numero
num1 = 3.895
print(math.ceil(num1)) # Arredondando para cima
print(math.floor(num1)) # Arredondando para baixo

# 4 - Calculando fatorial de numero
num2 = 7
print(math.factorial(num2))